class ReLU(TensorOp):
    def compute(self, a):
        return array_api.where(a> 0, a, 0)

    def gradient(self, out_grad, node):
        realized = node.realize_cached_data().copy()
        realized[realized > 0] = 1
        return out_grad * Tensor(realized)
        


def relu(a):
    return ReLU()(a)

