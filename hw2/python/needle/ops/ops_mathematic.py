class BroadcastTo(TensorOp):
    def __init__(self, shape):
        self.shape = shape

    def compute(self, a):
        return array_api.broadcast_to(a, self.shape)

    def gradient(self, out_grad, node):
        ori_shape = node.inputs[0].shape
        shrink_dims = [i for i in range(len(self.shape))]
        for i, (ori, cur) in enumerate(zip(reversed(ori_shape), reversed(self.shape))):
            if ori == cur:
                shrink_dims[len(self.shape) - i - 1] = -1
        shrink_dims = tuple(filter(lambda x: x >= 0, shrink_dims))
        return out_grad.sum(shrink_dims).reshape(ori_shape)


def broadcast_to(a, shape):
    return BroadcastTo(shape)(a)


class Summation(TensorOp):
    def __init__(self, axes: Optional[tuple] = None):
        self.axes = axes

    def compute(self, a):
        return array_api.sum(a, axis=self.axes)

    def gradient(self, out_grad, node):
        # tuple to list first.
        in_shape = list(node.inputs[0].shape)
        if self.axes is None:
            axes = range(len(in_shape))
        else:
            axes = self.axes
        for i in axes:
            in_shape[i] = 1
        # first reshape into original, then broadcast
        # (2,) to (2,1,1) => [[x1, 0, 0], [x2, 0, 0]]
        # then broadcast
        return out_grad.reshape(in_shape).broadcast_to(node.inputs[0].shape)


def summation(a, axes=None):
    return Summation(axes)(a)


class MatMul(TensorOp):
    def compute(self, a, b):
        return array_api.matmul(a, b)

    def gradient(self, out_grad, node):
        a, b = node.inputs
        grad_a = matmul(out_grad, b.transpose())
        grad_b = matmul(a.transpose(), out_grad)
        if len(a.shape) < len(grad_a.shape):
            grad_a = grad_a.sum(tuple([i for i in range(len(grad_a.shape) - len(a.shape))]))
        if len(b.shape) < len(grad_b.shape):
            grad_b = grad_b.sum(tuple([i for i in range(len(grad_b.shape) - len(b.shape))]))
        return grad_a, grad_b

def matmul(a, b):
    return MatMul()(a, b)


